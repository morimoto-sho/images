import random

def determine_nurse_type(answers):
    # 特定の質問に「はい」と答えた場合の処理
    if answers["あなたは今転職を考えていますか？"] == 'y' or answers["あなたは今の職場に不満を感じていますか？"] == 'y':
        return "マルチタイプ看護師"

    # それ以外の場合はスコアを基にタイプを決定（サンプルでは省略）
    # 本来はここで各タイプにスコアをつけ、最も高いスコアのタイプを選定
    return "具体的な看護師タイプ"

